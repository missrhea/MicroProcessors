DATA SEGMENT
	NUM1 DB 00H
	NUM2 DB 01H
	NUM3 DB ?
	MSG DB 10,13, "    $"
	MSG1 DB 10,13, "ENTER LENGTH OF SERIES:$"
	LEN DB ?
DATA ENDS
CODE SEGMENT
ASSUME DS:DATA, CS:CODE
START:
		MOV AX,DATA
		MOV DS,AX
		
		LEA DX,MSG1
		MOV AH,9
		INT 21H
		
		MOV AH,1
		INT 21H
		CMP AL,41H
		JC LAB
		SUB AL,07H
LAB:	SUB AL,30H
		MOV LEN,AL
		
		DEC LEN
		DEC LEN
		
		MOV AL,NUM1
		MOV NUM3,AL
		CALL PRINT
		
		MOV BL,NUM2
		MOV NUM3,BL
		CALL PRINT
		

		MOV CL,LEN
AGAIN:	MOV AL,NUM1
		MOV BL,NUM2
		ADD AL,BL
		MOV NUM3,AL				;NUM3=NUM2+NUM1 
		MOV NUM1,BL
		MOV NUM2,AL
		CALL PRINT
		DEC CL
		JNZ AGAIN
		
		MOV AH,4CH
		INT 21H
		
		
		
		PRINT PROC
			LEA DX,MSG
			MOV AH,9
			INT 21H
			
			MOV DL,NUM3
			AND DL,0F0H
			ROR DL,04H
			CMP DL,0AH
			JC LAB1
			ADD DL,07H
LAB1:		ADD DL,30H
			MOV AH,2
			INT 21H
			MOV DL,NUM3
			AND DL,0FH
			CMP DL,0AH
			JC LAB2
			ADD DL,07H
LAB2:		ADD DL,30H
			MOV AH,2
			INT 21H	
			
			RET
		ENDP	
CODE ENDS
END START